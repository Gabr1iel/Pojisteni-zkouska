function hamburger() {
    var x = document.getElementById("js-hamburger-links");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
}

function hamburgerSlide() {
    var x = document.getElementById("js-hamburger-links-slide");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
}